//Restrictions du formulaire de contact
const form = document.getElementById("formulaire");
const firstNameInput = document.getElementById("firstName");
const lastNameInput = document.getElementById("lastName");
const phoneNumberInput = document.getElementById("phoneNumber");
const emailInput = document.getElementById("email");
const messageInput = document.getElementById("message");

form.addEventListener("submit", (e) => {
  e.preventDefault(); //La méthode preventDefault() est une fonction JavaScript qui permet d'empêcher le comportement par défaut d'un événement déclenché par l'utilisateur sur une page web.
  //Lorsqu'un utilisateur effectue une action sur une page web, comme cliquer sur un lien ou soumettre un formulaire, un événement est déclenché. Par défaut, le navigateur va alors exécuter une action spécifique, comme naviguer vers une autre page ou soumettre le formulaire.
  //En appelant la méthode preventDefault() sur cet événement, vous pouvez empêcher ce comportement par défaut de se produire. Cela peut être utile si vous souhaitez par exemple valider des données avant de permettre à un formulaire d'être soumis, ou si vous voulez exécuter une action personnalisée lorsque l'utilisateur clique sur un lien.

  if (!isValidLastName(lastNameInput.value)) {
    //is valid vérifie si une chaîne de caractères contient uniquement des lettres et des chiffres
    alert("Le nom doit comporter au moins 3 caractères.");
    lastNameInput.focus();
    return false;
  }

  if (!isValidFirstName(firstNameInput.value)) {
    alert("Le prénom doit comporter au moins 3 caractères.");
    firstNameInput.focus();
    return false;
  }

  if (!isValidPhoneNumber(phoneNumberInput.value)) {
    alert("Le numéro de téléphone doit comporter au moins 10 chiffres");
    phoneNumberInput.focus();
    return false;
  }

  if (!isValidEmail(emailInput.value)) {
    alert("L'email n'est pas valide.");
    emailInput.focus();
    return false;
  }

  if (!isValidMessage(messageInput.value)) {
    alert("Le message doit comporter au moins 10 caractères.");
    messageInput.focus();
    return false;
  }

  // Envoyer le formulaire si tout est valide
  form.submit();
});

function isValidLastName(lastName) {
  return lastName.length >= 3;
}

function isValidFirstName(firstName) {
  return firstName.length >= 3;
}

function isValidPhoneNumber(phoneNumber) {
  return phoneNumber.length >= 10;
}

function isValidEmail(email) {
  const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
  return emailRegex.test(email);
}

function isValidMessage(message) {
  return message.length >= 10;
}
